  %    neighbors of the cells in the rightmost column are those in the
  %    leftmost column; likewise, the neighbors of the top row are the elements
  %    in the bottom row.
  %
  %    Assumes that X is a square matrix.
  %    X should be returned as a double()

  X = X
end

